use std::any::{Any, TypeId};
use std::pin::Pin;

use tokio_util::sync::CancellationToken;

use crate::runtime::DynRuntime;
use crate::test_case::BoxError;

type BoxFuture<T> = Pin<Box<dyn Future<Output = T> + Send + 'static>>;

/// Creates a concrete runtime instance, returning it as a boxed [`DynRuntime`].
pub type MakeRuntimeFn = fn() -> Result<Box<dyn DynRuntime>, BoxError>;

/// Creates the global context from the (leaked, `'static`) runtime and the
/// runner's root [`CancellationToken`].
pub type MakeGlobalFn = fn(
    &'static dyn DynRuntime,
    CancellationToken,
) -> BoxFuture<Result<Box<dyn Any + Send + Sync>, BoxError>>;

/// Creates a per-test context from the (leaked, `'static`) global context,
/// given a per-test [`CancellationToken`] scoped by the runner.
pub type MakeTestCtxFn = fn(
    &'static (dyn Any + Send + Sync),
    CancellationToken,
) -> BoxFuture<Result<Box<dyn Any + Send>, BoxError>>;

/// Runs the test body, borrowing the (leaked, `'static`) test context mutably.
pub type RunTestFn = fn(&'static mut (dyn Any + Send)) -> BoxFuture<Result<(), BoxError>>;

/// Tears down a per-test context after the test body returns.
pub type TeardownTestFn = fn(Box<dyn Any + Send>) -> BoxFuture<Result<(), BoxError>>;

/// Tears down the global context after all tests in the group finish.
pub type TeardownGlobalFn = fn(Box<dyn Any + Send + Sync>) -> BoxFuture<Result<(), BoxError>>;

/// Returns the `TypeId` that identifies a `(RuntimeType, GlobalType)` group.
/// Stored as a fn pointer because `TypeId::of::<T>()` is not yet `const`-stable.
pub type RuntimeGroupFn = fn() -> TypeId;

/// A self-contained, type-erased descriptor for a single test.
///
/// Generated by `#[rudzio::suite]` and registered in the global linkme slice so
/// that `rudzio::run()` can discover all tests at process startup without
/// needing to know about any module structure.
#[derive(Clone, Copy, Debug)]
pub struct TestToken {
    pub name: &'static str,
    pub ignored: bool,
    pub ignore_reason: &'static str,
    /// Source file path, used to sort tests into stable source order.
    pub file: &'static str,
    /// Source line number, used to sort tests into stable source order.
    pub line: u32,
    /// Returns the TypeId that identifies the `(RuntimeType, GlobalType)` group.
    /// Tests with the same value share one runtime and one global context.
    pub runtime_group: RuntimeGroupFn,
    pub runtime_name: &'static str,
    pub make_runtime: MakeRuntimeFn,
    pub make_global: MakeGlobalFn,
    pub make_test_ctx: MakeTestCtxFn,
    pub run: RunTestFn,
    pub teardown_test: TeardownTestFn,
    pub teardown_global: TeardownGlobalFn,
}

#[::linkme::distributed_slice]
#[doc(hidden)]
pub static TEST_TOKENS: [TestToken] = [..];
